#!/usr/bin/env python3
"""Generate per-batch CMakeLists.txt for the full dataset iteration 1.

Each batch builds 15 configs (N_CONFIG=15) against one floorplan.
Benign batches: 8 base apps + 7 nodbg variants = 15 entries
Standalone batches: 15 RO-count variants

Usage:
    python3 gen_cmakelists.py --batch BENIGN_FP00 --output builds/BENIGN_FP00/hw/CMakeLists.txt
    python3 gen_cmakelists.py --all   # generate all 10 batches
"""

import argparse
import os

BASE = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

# Floorplan mapping
FLOORPLANS = {
    "FP00": "FP00_full.xdc",
    "FP01": "FP01_lower_mid.xdc",
    "FP02": "FP02_lower_right.xdc",
    "FP03": "FP03_upper_left_trim.xdc",
    "FP04": "FP04_upper_right_trim.xdc",
}

# Benign app order (15 entries: 8 base + 7 nodbg)
BENIGN_APPS = [
    ("benign/A01_hello_world",            "A01 hello_world"),
    ("benign/A02_hls_vadd",               "A02 hls_vadd"),
    ("benign/A03_multitenancy_aes",       "A03 multitenancy_aes"),
    ("benign/A04_user_interrupts",        "A04 user_interrupts"),
    ("benign/A05_perf_fpga",              "A05 perf_fpga"),
    ("benign/A06_multithreading_aes",     "A06 multithreading_aes"),
    ("benign/A07_euclidean",              "A07 euclidean"),
    ("benign/A08_cosine",                 "A08 cosine"),
    ("benign_variants/V01_hello_world_nodbg",        "V01 hello_world nodbg"),
    ("benign_variants/V02_hls_vadd_nodbg",           "V02 hls_vadd nodbg"),
    ("benign_variants/V03_multitenancy_aes_nodbg",   "V03 multitenancy_aes nodbg"),
    ("benign_variants/V04_user_interrupts_nodbg",    "V04 user_interrupts nodbg"),
    ("benign_variants/V05_perf_fpga_nodbg",          "V05 perf_fpga nodbg"),
    ("benign_variants/V06_multithreading_aes_nodbg", "V06 multithreading_aes nodbg"),
    ("benign_variants/V07_euclidean_nodbg",          "V07 euclidean nodbg"),
]

# Standalone app order (15 RO counts)
RO_COUNTS = [4, 16, 64, 256, 1024, 4096, 8192, 10000, 12000, 14000, 16000, 18000, 19000, 20000, 22000]
STANDALONE_APPS = [
    (f"standalone/ro_{nro:04d}", f"standalone ro_{nro:04d} (N_RO={nro})")
    for nro in RO_COUNTS
]

# All batch definitions
BATCHES = {}
for fp_id in FLOORPLANS:
    BATCHES[f"BENIGN_{fp_id}"] = ("benign", fp_id, BENIGN_APPS)
    BATCHES[f"STAND_{fp_id}"] = ("standalone", fp_id, STANDALONE_APPS)

TEMPLATE = """\
######################################################################################
# Full dataset iteration 1 — {batch_id}
# Class: {class_name} | Floorplan: {fp_id} | 15 configs, 1 region each
#
# Auto-generated by gen_cmakelists.py — do not edit manually
######################################################################################

cmake_minimum_required(VERSION 3.5)
set(CYT_DIR ${{CMAKE_SOURCE_DIR}}/../../../../../)
set(CMAKE_MODULE_PATH ${{CMAKE_MODULE_PATH}} ${{CYT_DIR}}/cmake)
find_package(CoyoteHW REQUIRED)

project({batch_id})
message("*** Full dataset it1: {batch_id} ***")

# Shell parameters — FROZEN across all batches
set(FDEV_NAME   "u55c")
set(N_REGIONS   1)
set(EN_PR       1)
set(N_CONFIG    15)
set(EN_STRM     1)
set(N_STRM_AXI  2)
set(EN_MEM      0)

# Floorplan for this batch
set(FPLAN_PATH "${{CMAKE_SOURCE_DIR}}/floorplans/{fplan_file}")

validation_checks_hw()

load_apps(
{load_apps_body}
)

create_hw()
"""


def gen_load_apps(apps):
    lines = []
    for i, (path, desc) in enumerate(apps):
        lines.append(f'    VFPGA_C{i}_0   "apps/{path}"   # {desc}')
    return "\n".join(lines)


def gen_batch(batch_id, output_path):
    if batch_id not in BATCHES:
        print(f"ERROR: Unknown batch '{batch_id}'. Valid: {', '.join(sorted(BATCHES))}")
        return False

    class_name, fp_id, apps = BATCHES[batch_id]
    fplan_file = FLOORPLANS[fp_id]

    content = TEMPLATE.format(
        batch_id=batch_id,
        class_name=class_name,
        fp_id=fp_id,
        fplan_file=fplan_file,
        load_apps_body=gen_load_apps(apps),
    )

    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w") as f:
        f.write(content)
    print(f"  Generated {output_path}")
    return True


def main():
    parser = argparse.ArgumentParser(description="Generate per-batch CMakeLists.txt")
    parser.add_argument("--batch", help="Batch ID (e.g., BENIGN_FP00)")
    parser.add_argument("--output", help="Output path for CMakeLists.txt")
    parser.add_argument("--all", action="store_true", help="Generate all 10 batches")
    args = parser.parse_args()

    if args.all:
        for batch_id in sorted(BATCHES):
            output = os.path.join(BASE, "builds", batch_id, "hw", "CMakeLists.txt")
            gen_batch(batch_id, output)
        print(f"\nGenerated CMakeLists.txt for {len(BATCHES)} batches")
    elif args.batch and args.output:
        gen_batch(args.batch, args.output)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
