#!/usr/bin/env python3
"""Generate per-batch CMakeLists.txt for the big-hammer RO dataset."""

import argparse
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from dataset_config import (BASE, FLOORPLANS, ITERATION_LABEL, SHELL_PARAMS,
                            STANDALONE_APPS)


BATCHES = {
    f"STAND_{fp_id}": ("standalone", fp_id, STANDALONE_APPS)
    for fp_id in FLOORPLANS
}

TEMPLATE = """\
######################################################################################
# {label} - {{batch_id}}
# Class: {{class_name}} | Floorplan: {{fp_id}} | {{n_config}} configs, 1 region each
#
# Auto-generated by gen_cmakelists.py - do not edit manually
######################################################################################

cmake_minimum_required(VERSION 3.5)
set(CYT_DIR ${{{{CMAKE_SOURCE_DIR}}}}/../../../../../)
set(CMAKE_MODULE_PATH ${{{{CMAKE_MODULE_PATH}}}} ${{{{CYT_DIR}}}}/cmake)
find_package(CoyoteHW REQUIRED)

project({{batch_id}})
message("*** {label}: {{batch_id}} ***")

# Shell parameters
set(FDEV_NAME   "{{fdev_name}}")
set(N_REGIONS   {{n_regions}})
set(EN_PR       {{en_pr}})
set(N_CONFIG    {{n_config}})
set(EN_STRM     {{en_strm}})
set(N_STRM_AXI  {{n_strm_axi}})
set(EN_MEM      {{en_mem}})

# Floorplan for this batch
set(FPLAN_PATH "${{{{CMAKE_SOURCE_DIR}}}}/floorplans/{{fplan_file}}")

validation_checks_hw()

load_apps(
{{load_apps_body}}
)

create_hw()
""".format(label=ITERATION_LABEL)


def gen_load_apps(apps):
    lines = []
    for i, (path, desc) in enumerate(apps):
        lines.append(f'    VFPGA_C{i}_0   "apps/{path}"   # {desc}')
    return "\n".join(lines)


def gen_batch(batch_id, output_path):
    if batch_id not in BATCHES:
        print(f"ERROR: Unknown batch '{batch_id}'. Valid: {', '.join(sorted(BATCHES))}")
        return False

    class_name, fp_id, apps = BATCHES[batch_id]
    fplan_file = FLOORPLANS[fp_id]

    content = TEMPLATE.format(
        batch_id=batch_id,
        class_name=class_name,
        fp_id=fp_id,
        fplan_file=fplan_file,
        load_apps_body=gen_load_apps(apps),
        fdev_name=SHELL_PARAMS["FDEV_NAME"],
        n_regions=SHELL_PARAMS["N_REGIONS"],
        en_pr=SHELL_PARAMS["EN_PR"],
        n_config=SHELL_PARAMS["N_CONFIG"],
        en_strm=SHELL_PARAMS["EN_STRM"],
        n_strm_axi=SHELL_PARAMS["N_STRM_AXI"],
        en_mem=SHELL_PARAMS["EN_MEM"],
    )

    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w") as f:
        f.write(content)
    print(f"  Generated {output_path}")
    return True


def main():
    parser = argparse.ArgumentParser(description="Generate per-batch CMakeLists.txt")
    parser.add_argument("--batch", help="Batch ID (e.g., STAND_FP09)")
    parser.add_argument("--output", help="Output path for CMakeLists.txt")
    parser.add_argument("--all", action="store_true", help="Generate all batches")
    args = parser.parse_args()

    if args.all:
        for batch_id in sorted(BATCHES):
            output = os.path.join(BASE, "builds", batch_id, "hw", "CMakeLists.txt")
            gen_batch(batch_id, output)
        print(f"\nGenerated CMakeLists.txt for {len(BATCHES)} batches")
    elif args.batch and args.output:
        gen_batch(args.batch, args.output)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
